/**
 * Sanitize LLM output to enforce examiner rules:
 * - Strip banned phrases
 * - Enforce single sentence
 * - Cap word count
 * - Remove reaction prefixes
 */
function sanitizeExaminerOutput(text) {
  let cleaned = text.trim();

  // Strip any reaction/acknowledgment prefix
  cleaned = cleaned.replace(/^(?:Thank you\.|Thanks\.|Okay\.|Right\.|Alright\.|I see\.|Okay, thank you\.)\s*/i, "");

  // Strip banned patterns
  for (const pattern of BANNED_PATTERNS) {
    cleaned = cleaned.replace(pattern, "");
  }

  // Clean up double spaces and leading/trailing whitespace
  cleaned = cleaned.replace(/\s{2,}/g, " ").trim();

  // Enforce single sentence: take only the first sentence that ends with ? or .
  const sentenceMatch = cleaned.match(/^[^.?!]+[.?!]/);
  if (sentenceMatch) {
    cleaned = sentenceMatch[0].trim();
  }

  // Cap word count — if too long, truncate to last complete phrase
  const words = cleaned.split(/\s+/);
  if (words.length > MAX_QUESTION_WORDS) {
    cleaned = words.slice(0, MAX_QUESTION_WORDS).join(" ");
    // Ensure it ends with a question mark if it looks like a question
    if (!cleaned.endsWith("?") && !cleaned.endsWith(".")) {
      cleaned += "?";
    }
  }

  // Final safety: examiner output must be a question
  if (cleaned && !cleaned.endsWith("?")) {
    // If it ends with a period, replace with question mark
    if (cleaned.endsWith(".")) {
      cleaned = cleaned.slice(0, -1) + "?";
    } else {
      cleaned += "?";
    }
  }

  return cleaned || "What is your opinion on that?";
}

