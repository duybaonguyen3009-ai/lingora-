/**
 * Get the current topic set for the active Part 1 block.
 */
function getCurrentTopicSet(state) {
  const blockIdx = state.currentTopicBlock || 0;
  const indices = state.topicSetIndices || [0, 1];
  const topicIdx = indices[Math.min(blockIdx, indices.length - 1)];
  return IELTS_PART1_TOPIC_SETS[topicIdx % IELTS_PART1_TOPIC_SETS.length];
}

/**
 * Get the question hint for the current position within the current topic block.
 */
function getQuestionHint(state) {
  const topicSet = getCurrentTopicSet(state);
  const posInBlock = state.questionIndex % IELTS_PART1_QUESTIONS_PER_BLOCK;
  return topicSet.questions[Math.min(posInBlock, topicSet.questions.length - 1)];
}

/**
 * Check if we're at the start of a new topic block (topic transition needed).
 */
function isTopicTransition(state) {
  return state.questionIndex > 0 && state.questionIndex % IELTS_PART1_QUESTIONS_PER_BLOCK === 0;
}

/**
 * CONTROLLED EXAMINER PROMPT — minimal, no personality, no reactions.
 *
 * The examiner is NOT conversational. It generates exactly ONE utterance:
 * either a single question or nothing (transitions are hardcoded).
 *
 * Output contract:
 * - ONE sentence
 * - MAX 12 words (for questions)
 * - ZERO reactions to user answers
 * - ZERO praise, acknowledgment, or filler
 */
function buildIeltsSystemPrompt(state) {
  const cueCard = IELTS_CUE_CARDS[state.cueCardIndex % IELTS_CUE_CARDS.length];

  const base = `You generate IELTS Speaking examiner questions. Output ONLY the question — nothing else.

ABSOLUTE RULES:
- Output exactly ONE question. No commentary, no filler.
- Maximum 12 words.
- NEVER react to the candidate's answer. No "interesting", "good", "I see", "thank you".
- NEVER use soft framing: no "Can you tell me", "I'd like to ask", "Let's talk about".
- NEVER explain the test or mention which part it is.
- Be direct: "Where do you live?" not "Could you describe where you live?"`;

  // ── Opening — Name request ──
  if (state.part === 1 && state.phase === "opening") {
    return `${base}

OUTPUT: Ask the candidate for their full name. Nothing else.
Example: "What is your full name?"`;
  }

  // ── Part 1: Interview Questions ──
  if (state.part === 1 && state.phase === "question") {
    const topicSet = getCurrentTopicSet(state);
    const questionHint = getQuestionHint(state);

    return `${base}

Topic: "${topicSet.theme}"
Guide question (rephrase, do not copy): "${questionHint}"

OUTPUT: One direct question about "${topicSet.theme}". Maximum 12 words.`;
  }

  // ── Part 2: follow-up ──
  if (state.part === 2 && state.phase === "follow_up") {
    const part2Response = (state.userResponses || []).slice(-1)[0] || "";
    const part2Summary = part2Response.length > 150 ? part2Response.substring(0, 150) + "..." : part2Response;

    return `${base}

The candidate spoke about: "${cueCard.topic}"
What they said: "${part2Summary}"

OUTPUT: One brief follow-up question about what the candidate said. Maximum 12 words.`;
  }

  // ── Part 3: discussion questions with DISCUSSION LADDER ──
  if (state.part === 3 && state.phase === "question_p3") {
    const analysis = analyzeResponseQuality(state);
    const themeLabel = cueCard.part3Theme || cueCard.topic.toLowerCase().replace("describe ", "");
    const tier = PART3_TIERS[state.part3Tier || 0] || "concrete";

    let tierHint;
    switch (tier) {
      case "concrete":
        tierHint = "Ask about observable facts or common behaviors.";
        break;
      case "comparative":
        tierHint = "Ask to compare across time, places, or groups.";
        break;
      case "analytical":
        tierHint = "Ask about causes, effects, or reasons.";
        break;
      case "evaluative":
        tierHint = "Ask to evaluate, judge, or predict.";
        break;
    }

    let adaptiveNote = "";
    if (analysis.level === "strong") {
      adaptiveNote = " Use nuanced phrasing.";
    } else if (analysis.level === "limited") {
      adaptiveNote = " Use simple phrasing.";
    }

    return `${base}

Theme: "${themeLabel}"
Tier: ${tier.toUpperCase()} — ${tierHint}${adaptiveNote}

OUTPUT: One discussion question about "${themeLabel}". Maximum 15 words.`;
  }

  return base;
}

